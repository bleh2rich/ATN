local pid = "301e4b7fecc2d17f1d38d516d5fbdb42"
local currentlink = "discord.gg/K823qJDKAF"
local function kick_player()
    local message = "No key found, Please join the discord server to retrieve your key, it has been set to your clipboard. " .. currentlink
    
    if setclipboard then
        setclipboard("https://" .. currentlink)
    end
    
    game:GetService("Players").LocalPlayer:Kick(message)
end
local function validate_key(user_key)
    local success, response = pcall(function()
        local url = "https://api.luarmor.net/v3/projects/" .. pid .. "/users?user_key=" .. user_key
        local http_service = game:GetService("HttpService")
        
        local result = http_service:GetAsync(url)
        local data = http_service:JSONDecode(result)
        
        if data.success and data.users and #data.users > 0 then
            local user = data.users[1]
            if user.status == "active" and user.banned == 0 then
                if user.auth_expire == -1 or user.auth_expire > os.time() then
                    return true
                end
            end
        end
        
        return false
    end)
    
    return success and response
end
local function authenticate_user()
    if not script_key or script_key == "" then
        kick_player()
        return false
    end
    
    if validate_key(script_key) then
        return true
    else
        kick_player()
        return false
    end
end
if authenticate_user() then
    loadstring(game:HttpGet("https://api.luarmor.net/files/v3/loaders/301e4b7fecc2d17f1d38d516d5fbdb42.lua"))()
end
